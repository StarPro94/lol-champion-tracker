/**
 * Retourne les rôles par défaut d'un champion
 * Si le champion n'est pas dans le mapping, retourne un tableau vide
 */
export function getDefaultChampionRoles(championId: string): LaneRole[] {
  return DEFAULT_CHAMPION_ROLES[championId] || [];
}

/**
 * Retourne tous les champions d'un rôle spécifique
 */
export function getChampionsByRole(role: LaneRole): string[] {
  return Object.entries(DEFAULT_CHAMPION_ROLES)
    .filter(([_, roles]) => roles.includes(role))
    .map(([championId, _]) => championId);
}
